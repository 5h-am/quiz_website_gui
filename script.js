function sub_category_generator(catg){
	let subcategories = Object.keys(records[`${catg}_questions`]);
	return subcategories;
}

function randomness (ques,opt,ans) {
	for (let i = ques.length-1; i > 0; i--) {
		let random = Math.floor(Math.random() * (i+1));
		[ques[i], ques[random]] = [ques[random], ques[i]];
		[opt[i], opt[random]] = [opt[random], opt[i]];
		[ans[i], ans[random]] = [ans[random], ans[i]];
	}
	return [ques,opt,ans];

}

function answervalidation (options,correct,userinput) {
	const index = ["a","b","c","d"].indexOf(userinput);
	if (index === -1) return NaN;
	return options[index].replaceAll(" ", "").toLowerCase() === correct.toLowerCase().replaceAll(" ","") ? 1 : NaN;
}


function quizGenerator (category,sub_category) {
	let questions = records[`${category}_questions`][`${sub_category}`];
	let options = records[`${category}_options`][`${sub_category}`];
	let answers = records[`${category}_answers`][`${sub_category}`];
	let randoms = randomness(questions,options,answers);
	shuffled_questions = randoms[0];
	shuffled_options = randoms[1];
	shuffled_answers = randoms[2];
	return [shuffled_questions,shuffled_options,shuffled_answers];
}


